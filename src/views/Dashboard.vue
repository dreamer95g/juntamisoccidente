//import WordExport from "./juntamisoikos/export/WordExport";
import Slider from "./Slider";
import gql from "graphql-tag";
import HomeLine from "./juntamisoikos/charts/HomeLine";
import HomeBar from "./juntamisoikos/charts/HomeBar";
export default {
  data() {
    return {
      members: "0",
      kids: "0",
      teens: "0",
      youngs: "0",
      men: "0",
      ladies: "0",

      first_year: "",
      secondly_year: "",
      third_year: "",
      fourth_year: "",
      last_year: "",
      faith_profession: "",
      baptism: "",

      token: "",
    };
  },
  mounted() {
    this.token = this.$store.state.token;
    this.setValues();
    this.notify(
      "success",
      "Bienvenido",
      "Bienvenido al sistema de Junta de Misiones de Occidente"
    );
    this.$store.commit("set", ["layout", "default-layout"]);
  },

  methods: {
    async setValues() {
      await this.$apollo
        .query({
          query: gql`
            query {
              homeAnalytica {
                members
                teens
                youngs
                men
                ladies
                kids
                first_year
                secondly_year
                third_year
                fourth_year
                last_year
                faith_profession
                baptism
              }
            }
          `,
          context: {
            headers: { Authorization: this.token !== "" ? "Bearer " + this.token : "" },
          },
          fetchPolicy: "no-cache",
        })
        .then((data) => {
          this.members = data.data.homeAnalytica.members;
          this.kids = data.data.homeAnalytica.kids;
          this.teens = data.data.homeAnalytica.teens;
          this.youngs = data.data.homeAnalytica.youngs;
          this.men = data.data.homeAnalytica.men;
          this.ladies = data.data.homeAnalytica.ladies;
          this.first_year = data.data.homeAnalytica.first_year;
          this.secondly_year = data.data.homeAnalytica.secondly_year;
          this.third_year = data.data.homeAnalytica.third_year;
          this.fourth_year = data.data.homeAnalytica.fourth_year;
          this.last_year = data.data.homeAnalytica.last_year;
          this.faith_profession = data.data.homeAnalytica.faith_profession;
          this.baptism = data.data.homeAnalytica.baptism;
        })
        .catch((error) => {
          console.error(error);
        });
    },

    notify(type, title, description) {
      //TYPES
      //success, info, warning, error
      this.$notification[type]({
        message: title,
        description: description,
      });
    },
  },
  components: {
    //WordExport: WordExport,
    Slider: Slider,
    HomeLine: HomeLine,
    HomeBar: HomeBar,
  },
};
</script>
